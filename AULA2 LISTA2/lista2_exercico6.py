'''
Exercício 6: Calculando a média
Crie três variáveis chamadas nota1, nota2 e nota3 e atribua valores numéricos a elas.
Calcule a média dessas notas e armazene o resultado em media. Por fim, exiba a seguinte mensagem formatada:
"A média das notas [nota1], [nota2] e [nota3] é [media]."
'''
nota1=5
nota2=4
nota3=10
media=float((nota1+nota2+nota3)/3)

print(f'A média das notas {nota1}, {nota2} e {nota3} é {round(media,2)}')